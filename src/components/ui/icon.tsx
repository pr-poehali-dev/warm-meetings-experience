// Алиасы для удобства / совместимости со старыми именами иконок.
ICONS.Verified = BadgeCheck;
ICONS.LinkedinIcon = Linkedin;

// Кэш для иконок, подгружаемых динамически (если кто-то использует редкое имя).
const dynamicCache: Record<string, LucideIcon | null> = {};

interface IconProps extends LucideProps {
  name: string;
  fallback?: string;
}

const Icon: React.FC<IconProps> = ({ name, fallback = 'CircleAlert', ...props }) => {
  const Static = ICONS[name];
  const [Dynamic, setDynamic] = useState<LucideIcon | null>(() => dynamicCache[name] ?? null);

  useEffect(() => {
    if (Static || dynamicCache[name] !== undefined) return;
    let cancelled = false;
    import('lucide-react')
      .then((mod) => {
        const Comp = (mod as unknown as Record<string, LucideIcon>)[name] ?? null;
        dynamicCache[name] = Comp;
        if (!cancelled) setDynamic(() => Comp);
      })
      .catch(() => {
        dynamicCache[name] = null;
      });
    return () => {
      cancelled = true;
    };
  }, [name, Static]);

  const Component = Static || Dynamic;
  if (Component) return <Component {...props} />;

  // Иконка ещё грузится или не существует — показываем fallback из статического спрайта.
  const Fallback = ICONS[fallback] ?? ICONS.CircleAlert;
  if (Fallback) return <Fallback {...props} />;

  return <span className="text-xs text-gray-400">[icon]</span>;
};

export default Icon;